package FourthLecture;

public class WHILELOOP {
    public static void main(String[] args){
        int i = 2;//i=2; is initialisation;

        while(i<=20) //i<=20; is condition; 
        {

            System.out.println(i); //body;

            i++; //i++ (or i=i+1) is updation;
        }
    }
}
